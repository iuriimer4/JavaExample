package HillelSchool;

public class IsoscelesTriangle {
  public static void main(String[] args) {
    int a = 5;
    int b = 3;
    int c = 8;
    if (a == b || a == c || b == c) {
      System.out.println("This is the isosceles triangle");
    } else {
      System.out.println("This is not isosceles triangle");
    }
  }

}

