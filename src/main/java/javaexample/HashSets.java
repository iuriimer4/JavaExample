package javaexample;

import java.util.HashSet;

public class HashSets {
    public static void main(String[] args) {
        HashSet<String> omg = new HashSet<String>();
        omg.add("1");
        omg.add("2");
        omg.add("3");
        omg.add("4");
        System.out.println(omg);
    }
}
